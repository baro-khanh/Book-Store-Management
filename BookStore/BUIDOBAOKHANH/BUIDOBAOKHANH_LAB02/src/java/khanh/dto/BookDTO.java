package khanh.dto;

/**
 *
 * @author buido
 */
public class BookDTO {

    private int bookId, quantity, cateId;
    private float price;
    private boolean status;
    private String title, image, description, author, importDate, cateName;

    public BookDTO(int quantity, int cateId, float price, boolean status, String title, String image, String description, String author, String importDate) {
//        this.bookId = bookId;
        this.quantity = quantity;
        this.cateId = cateId;
        this.price = price;
        this.status = status;
        this.title = title;
        this.image = image;
        this.description = description;
        this.author = author;
        this.importDate = importDate;
    }

    public String getCateName() {
        return cateName;
    }

    public void setCateName(String cateName) {
        this.cateName = cateName;
    }

    public int getBookId() {
        return bookId;
    }

    public void setBookId(int bookId) {
        this.bookId = bookId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getCateId() {
        return cateId;
    }

    public void setCateId(int cateId) {
        this.cateId = cateId;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getImportDate() {
        return importDate;
    }

    public void setImportDate(String importDate) {
        this.importDate = importDate;
    }

}
